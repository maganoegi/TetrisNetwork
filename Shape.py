class Shape:
    def __init__(self, properties, array):
        # [0] => number of contact edges
        # [1] => number of rotational states
        self.properties = properties

        # [0] => string
        # [1] => score
        # [2] => rotational orientation:
        #               0: equivalent to name class
        #               1+ : clockwise rotations
        self.subStrings = array
    
    def get_number_edges(self):
        return self.properties[0]

    def getNbRotations(self):
        return self.properties[1]
    
    def getBestString(self):
        return self.subStrings[0][0]

